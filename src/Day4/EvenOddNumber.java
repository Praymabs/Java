package Day4;

public class EvenOddNumber {

	public static void main(String[] args) 
	{
		// example1
		int num=10;
		
		if(num%2==0)
		{
			System.out.println("It is Even number");
		}
		else
		{
			System.out.println("It is odd number");
		}

		// example2
		int num2=10;
				
		if(num2%2==1)
		{
			System.out.println("It is Odd number");
		}			
		else
		{			
			System.out.println("It is Even number");
		}
	}

}
