package Day3;

public class AssignmentOperators {

	public static void main(String[] args) 
	{
		// Assignment operator = += *= /= %=
		
		// example-1
		int a=10;
	    a+=5;  // a= a+5
		System.out.println(a);
		
		//example-2
		int b=10;
	    b-=5;  // a= a-5
		System.out.println(b);
		
		
		//example-3
		int c=10;
		c*=5; // c=c*5
		System.out.println(c);
		
		//example-4
		int d=10;
	    d/=5;  // a= a/5
		System.out.println(d);
		
		//example-5
		int e=10;
	    e%=5;  // a= a%5
		System.out.println(e);

	}

}
